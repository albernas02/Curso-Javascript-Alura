const estudante = "Caroline";
const estaAprovado = true;

if (estaAprovado) {
    console.log(`${estudante} está aprovado!`);
}
else {
    console.log(`${estudante} não está aprovada.`);
}

if (estudante === "Caroline") {
    console.log("O estudante é Caroline.");
}
else {
    console.log("O estudante não é Caroline.");
}